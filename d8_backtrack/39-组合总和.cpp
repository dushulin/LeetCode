#include <vector>
#include <iostream>
#include <algorithm>

using namespace std;

class Solution {
public:

    vector<vector<int>> result;
    vector<int> path;

    vector<vector<int>> combinationSum(vector<int>& candidates, int target) {
        int sum = 0;
        sort(candidates.begin(), candidates.end());
//        for (int i = 0; i < candidates.size(); ++i) {
//            cout << candidates[i] << endl;
//        }
        backTracing(sum, target, candidates, 0);
        return result;
    }

    void backTracing(int sum, int target, vector<int>& candidates, int startIndex){
        if (sum > target){
            return;
        }
        if (sum == target){
            result.push_back(path);
            return;
        }

        for (int i = startIndex; candidates[startIndex] <= target && i < candidates.size(); ++i) {
            path.push_back(candidates[i]);
            sum += candidates[i];
            backTracing(sum, target, candidates, i);
            sum -= candidates[i];
            path.pop_back();
        }
    }
};

int main(){
    Solution s;
    vector<int> v = {1};
    int target = 1;
    cout << s.combinationSum(v, target).size();
}

