/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */

#include <iostream>
#include <vector>
#include <stack>
using namespace std;

struct TreeNode{
    int val;
    TreeNode* left;
    TreeNode* right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};
class Solution {
public:
    vector<vector<int>> result;
    vector<vector<int>> pathSum(TreeNode* root, int targetSum) {
        if (root == NULL)
            return result;
        vector<int> oneResult;
        oneResult.push_back(root->val);
        transal(root, targetSum - root->val, oneResult);
        return result;
    }

    void transal(TreeNode* node, int count, vector<int>& oneResult){
        if (node->left == NULL && node->right == NULL && count == 0){
            result.push_back(oneResult);
        }
        if (node->left != NULL){
            count -= node->left->val;
            oneResult.push_back(node->left->val);
            transal(node->left, count, oneResult);
            oneResult.pop_back();
            count += node->left->val;
        }
        if (node->right != NULL){
            count -= node->right->val;
            oneResult.push_back(node->right->val);
            transal(node->right, count, oneResult);
            oneResult.pop_back();
            count += node->right->val;
        }
    }
};

int main(){
    Solution solution;
    TreeNode* root = new TreeNode(1);
    TreeNode* root1 = new TreeNode(2);
    TreeNode* root2 = new TreeNode(3);
    root->left = root1;
    root1->left = root2;
    cout << solution.pathSum(root, 6).size();
}
