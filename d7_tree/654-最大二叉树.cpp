/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */

#include <iostream>
#include <vector>
using namespace std;
struct TreeNode{
    int val;
    TreeNode* left;
    TreeNode* right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};
class Solution {
public:
    TreeNode* constructMaximumBinaryTree(vector<int>& nums) {
        if (nums.empty())
            return NULL;
        return transal(nums);
    }

    TreeNode* transal(vector<int>& nums){
        if (nums.empty())
            return nullptr;

        if (nums.size() == 1){
            return new TreeNode(nums[0]);
        }

        int rootValue = nums[0];
        int delimiter = 0;
        for (int i = 0; i < nums.size(); ++i) {
            if (rootValue < nums[i]){
                rootValue = nums[i];
                delimiter = i;
            }
        }
        auto* root = new TreeNode(rootValue);
        vector<int> left(nums.begin(), nums.begin() + delimiter);
        vector<int> right(nums.begin() + delimiter + 1, nums.end());

        cout << "----------------" << endl;
        cout << "left" << endl;
        for (int i = 0; i < left.size(); ++i) {
            cout << left[i] << ", ";
        }
        cout << endl;
        cout << "right" << endl;
        for (int i = 0; i < right.size(); ++i) {
            cout << right[i] << ", ";
        }
        cout << endl;

        root->left = transal(left);
        root->right = transal(right);

        return root;
    }
};

int main(){
    Solution s;
    vector<int> nums = {3,2,1,6,0,5};
    s.constructMaximumBinaryTree(nums);
}
